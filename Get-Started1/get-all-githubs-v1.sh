echo "################################################################"
echo "##             GIT CLONE ANY AND ALL GITHUBS               #####"
echo "################################################################"
echo

cd ~

count=0

for name in $(ls -d ARCOLINUX*/); do
	count=$[count+1]
	cd $name
	tput setaf 1;echo "Github "$count;tput sgr0;
	sh ./1-get-all*
	echo "#################################################"
	echo "################  "$(basename `pwd`)" done"
	echo "#################################################"
	cd ..
done


for name in $(ls -d CARLI*/); do
	count=$[count+1]
	cd $name
	tput setaf 1;echo "Github "$count;tput sgr0;
	sh ./1-get-all*
	echo "#################################################"
	echo "################  "$(basename `pwd`)" done"
	echo "#################################################"
	cd ..
done
